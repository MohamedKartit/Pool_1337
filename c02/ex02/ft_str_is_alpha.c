#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int	l;

	l = 0;
	while (str[l])
	{
		if (!(str[l] >= 65 && str[l] <= 90) && !(str[l] >= 97 && str[l] <= 122))
			return (0);
		l++;
	}
	return (1);
}
